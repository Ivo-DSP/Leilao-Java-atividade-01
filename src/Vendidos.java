    private void listarProdutosV(){
        try {
            ProdutosDAO produtosdao = new ProdutosDAO();
            
            boolean status3 = produtosdao.connectDB();
            if(status3){
                ArrayList<ProdutosDTO> listagem = produtosdao.listarProdutosV();
                
                DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
                model.setNumRows(0);
                listaProdutos.setRowSorter(new TableRowSorter(model));
            
                for(int i = 0; i < listagem.size(); i++){
                    model.addRow(new Object[]{
                        listagem.get(i).getId(),
                        listagem.get(i).getNome(),
                        listagem.get(i).getValor(),
                        listagem.get(i).getStatus()
                    });
                }
            } else{
             JOptionPane.showMessageDialog(null,"Erro de conexÃ£o");   
            }
        } catch (Exception e) {
        }
    
    }


